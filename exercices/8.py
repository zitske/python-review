##As notas de um aluno estão armazenadas em uma lista. Calcular a média dessas notas.
#
# Cria a lista
my_list = [5, 7, 11]

# Mostra a lista
print(my_list)

# Soma os valores da lista
result = sum(my_list)

# Mostra o resultado
print("A soma dos valores da lista é:", result)

# Calcula a média
media = result / len(my_list)

# Mostra a média
print("A média dos valores da lista é:", media)