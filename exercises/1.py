#Fazer um programa que realiza a soma de dois números inteiros e mostre o resultado com a função print.

# Input dos numeros
num1 = int(input("Insira o primeiro número: "))
num2 = int(input("Insira o segundo número: "))

# Faz a adição dos numeros
result = num1 + num2

# Mostra o resultado
print("O resultado da soma de", num1, "e", num2, "é:", result)




