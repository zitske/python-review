##Calcular um reajuste de 35% no salário de um funcionário, a partir de um valor dado.

# Input do salario
salario = float(input("Insira o salário: "))
# Calcula o reajuste
reajuste = salario * 0.35
# Calcula o novo salario
novo_salario = salario + reajuste
# Mostra o resultado
print("O novo salário é:", novo_salario)